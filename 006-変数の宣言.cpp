
#include <iostream>


int main() {
  // 変数を使おう
    
  // 変数の宣言
  // <型> <変数名>;
          
  // 今回は"整数"を扱う<型>
  // <int>を使用する
  int x; // int型の変数"x"を宣言
                  
  // xの値を出力
  std::cout << x << std::endl;
                        
  // 値の"代入"
  // <変数> = <数値>;
  // "="は"等しい"ではなく"代入"であることに注意
  x = 10; // int型の変数xに数値10を代入
                                  
  // xの値を出力
  std::cout << x << std::endl;
}
